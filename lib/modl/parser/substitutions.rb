# frozen_string_literal: true

require 'singleton'

module Modl::Parser
  class Substitutions
    include Singleton

    def initialize
      @subs = [
        ['~\\', '\\'],
        ['\\\\', '\\'],
        ['~~', '~'],
        ['\\~', '~'],
        ['~(', '('],
        ['\\(', '('],
        ['~)', ')'],
        ['\\)', ')'],
        ['~[', '['],
        ['\\[', '['],
        ['~]', ']'],
        ['\\]', ']'],
        ['~{', '{'],
        ['\\{', '{'],
        ['~}', '}'],
        ['\\}', '}'],
        ['~;', ';'],
        ['\\;', ';'],
        ['~:', ':'],
        ['\\:', ':'],
        ['~`', '`'],
        ['\\`', '`'],
        ['~"', '"'],
        ['\\"', '"'],
        ['~=', '='],
        ['\\=', '='],
        ['~/', '/'],
        ['\\/', '/'],
        ['<', '<'],
        ['\\<', '<'],
        ['~>', '>'],
        ['\\>', '>'],
        ['~&', '&'],
        ['\\&', '&'],
        ['!', '!'],
        ['\\!', '!'],
        ['~|', '|'],
        ['\\|', '|'],
        ['\\t', "\t"],
        ['\\n', "\n"],
        ['\\b', "\b"],
        ['\\f', "\f"],
        ['\\r', "\r"]
      ]
    end

    def process(str)
      prev = ''
      @subs.each do |s|
        loop do
          prev = str
          str = str.sub(s[0], s[1])
          break unless str && str != prev
        end
      end
      str
    end
  end
end
